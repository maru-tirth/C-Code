#include<stdio.h>
int main()
{
	int a,b;
	a=10;
	b=20;
	printf(" The value of a : %d \n " , a);
	printf("The value of b :%d \n ", b);
	a++;
	b++;
	printf("The incremented value of a : %d \n " , a);
	printf("The incremented value of b : %d \n " , b);
	a--;
	b--;
	printf("The decremented value of a : %d \n ", a);
	printf("The decremented value of b : %d \n " , b);
	return 0;
}
